    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int idcomanda=0;
        try {
            idcomanda=Integer.parseInt((String)jComboBox2.getSelectedItem());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        for (Comanda obj_it : LogicaComandes.getLlistaComandes()) {
            if ((obj_it.getId()== idcomanda) && (!obj_it.isPagada())) {
                obj_it.setPagada(true);
            }
        }
        Factura facturadialeg = new Factura(parent,true,mesa,idcomanda);
        facturadialeg.setVisible(true);
        taula.updateTable();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

