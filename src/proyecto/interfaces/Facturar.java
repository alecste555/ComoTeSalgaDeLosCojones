    public void initDropdown() {
        ArrayList<String> comandes = new ArrayList<>();
        for (Comanda obj_it : LogicaComandes.getLlistaComandes()) {
            if ((obj_it.getTaula() == mesa) && (!obj_it.isPagada())) {
                comandes.add(Integer.toString(obj_it.getId()));
            }
        }
        jComboBox2.setModel(new DefaultComboBoxModel<>(comandes.toArray(new String[0])));
    }

